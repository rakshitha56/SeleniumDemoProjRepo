package com.practicetestautomation.pageojects;

import org.openqa.selenium.By;
import org.openqa.selenium.WebDriver;
import org.openqa.selenium.WebElement;

public class LoginPage extends BasePage {  //inherits the basepage class
    //used to locate the element in web page
    private By usernameinputLocator = By.id("username");
    private By passwordinputLocator = By.id("password");
    private By submitButton = By.className("btn");
    private By errorMessageLocator =By.id("error");

    public LoginPage(WebDriver driver) {
        super(driver);
    }

    public void visit(){
       super.visit("https://practicetestautomation.com/practice-test-login/");
    }


    public void usernameiputfield(String username){
        driver.findElement(usernameinputLocator).sendKeys(username);
    }

    public void passinputfield(String password){
        driver.findElement(passwordinputLocator).sendKeys(password);
    }

    public void submitButtonfield(){
        driver.findElement(submitButton).click();
    }

    public SucessfulLogin execution(String username, String password){
        usernameiputfield(username);
        passinputfield(password);
        submitButtonfield();
        return new SucessfulLogin(driver);
    }

    public String errorMsg(){
     WebElement errMsg = waitforElement(errorMessageLocator);
        return errMsg.getText();
    }
}
























































































































































































































































































































































































































































































































































































































































































































































































































































